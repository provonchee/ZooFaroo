<div class="sectionHeaderFormat blueHeader"><h2 class="about-title">How does ZooFaroo work?</h2></div>
<div id="about-content">
<br/>
First, you register (don't worry, it's easy and free).&nbsp;&nbsp;During the process you will have the option of including links to your facebook page, twitter page, google plus page, linkedin page, and your own website.&nbsp;&nbsp;
<br/>
<center><img src="images/register.jpg"alt="ZooFaroo Register" width="620" height="800"/></center>
<br/>
<br/>
<br/>
These links will show up on your ZooFaroo User Page (viewable to the ZooFaroo community) along with your current posts and user reviews left for you by other users.&nbsp;&nbsp;PLEASE NOTE: Your email addres is <u><b>never</b></u> visible on the User Information Page.  We value your privacy and will never sell your information to anyone.
<br/>
<center><img src="images/userInfo.jpg"alt="ZooFaroo User Info" width="620" height="365"/></center>
<br/>
While the link information is optional we do encourage you to include at least one or two simply because it is a great way for ZooFaroo members to have a 'soft introduction' before an exchange takes place.
<br/>
<br/>
<br/>
Once registered, you can post as many 'offers' and 'needs' as you'd like.&nbsp;&nbsp;Do you need a lawnmower?&nbsp;&nbsp;Post that as a 'need'.&nbsp;&nbsp;Do you have a cottage to rent?&nbsp;&nbsp;Post that as an 'offer'.
<br/>
<center><img src="images/post.jpg"alt="ZooFaroo Post" width="620" height="137"/></center>
<br/> 
<br/>
<br/> 
And don't worry, you don't have to post eveything at once.&nbsp;&nbsp;You can post one 'offer' today and two 'needs' next week, it all gets saved to your User Account.
<br/>
<center><img src="images/postForm.jpg"alt="ZooFaroo Posting Form" width="620" height="454"/></center>
<br/> 
<br/>
<br/> 
We understand that sometimes you will need something where time is of the essence, or you just really want what you want, like tickets to the show that weekend.&nbsp;&nbsp;If that's the case just tick the 'in addition to trades I am willing to pay money for what I need' box&nbsp;<img src="images/money.png"alt="Open to paying for what they need" width="15" height="15"/>&nbsp; when filling out the post.&nbsp;&nbsp;No one wants you to miss the show this weekend!<br/>
<br/>
<center><img src="images/needPostMoney.jpg"alt="ZooFaroo Willing to Pay Post" width="620" height="272"/></center>
<br/>
<br/>
<br/>
Once your posts are active, interested ZooFaroo users will be able to browse your 'offers' and 'needs' independently.
<br/>
<center><img src="images/postList.jpg"alt="ZooFaroo Posting Offer Lists" width="620" height="141"/></center>
<center><img src="images/needList.jpg"alt="ZooFaroo Posting Need Lists" width="620" height="143"/></center>
<br/>
<br/>
<br/>
If users are interesteed in what you're 'offering' or what you 'need' in they'll 'Reply to your Post' with offers.&nbsp;&nbsp;In turn, as a registered user, you can feel free to browse other users 'offers' and 'needs' and reply as you like.
<br/>
<center><img src="images/postOffer.jpg"alt="ZooFaroo Posting Need Lists" width="620" height="606"/></center>
<br/>
<br/>
<br/>
If you're a business and you'd like to promote it, first make sure you fill out the optional info on the register page (facebook link, twitter link, link to your own website, etc) and then post an offer of what your company offers, e.g., pet care, house painting, cooking supplies, etc.&nbsp;&nbsp;If you're not interested in bartering or trade offers simply tick the 'only accept money offers box'&nbsp;<img src="images/onlyMoney.png"alt="Only Accepts Money Offers" width="15" height="15"/>&nbsp;when you post.
