<div class="sectionHeaderFormat blueHeader"><h2 class="about-title">What is ZooFaroo?</h2></div>
<div id="about-content">
ZooFaroo is an online social marketplace and resource where neighbors and small businesses can trade, buy, sell, barter, and promote themselves within in their community.
<br/><br/>

ZooFaroo has many uses:
<ol>
<li>It's a place for the private individual or small business to swap or sell their goods and services with their community.</li>
 <br/><br/>
<li>It's a great place to promote your small business or your talents (e.g. a aspiring artist, baby sitter, performer, carpenter, etc.) and tap into markets that otherwise would be out of reach.</li> 
<br/><br/>
<li>It's a fantastic place to have a 'soft' introduction through a user's ratings, reviews, facebook page, google plus page, or their own website. It's a way to get to know a little bit more about the person or business that you're potentially going to have an exchange with.</li>
</ol>
<br/>
Visit ZooFaroo to check out the many offers or needs left by neighbors or local businesses.  Take a moment to add a post or two of your own, then browse the site and leave some reviews.  Showcase your professional skills, or get creative and offer up that old refrigerator in your basement in exchange for help tiling an outdated bathroom!
<br/><br/>
ZooFaroo is a community that encourages everyone to help out their neighbors, share the benefits of hard work and see the benefits of reaching out to those in your community.  A single visit to ZooFaroo will show you how a fun functional online social marketplace can bring out the best in you and your community!</div>
